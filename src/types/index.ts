// ─── Direction ────────────────────────────────────────────────────────────────

/**
 * Направление движения ряда.
 *  1  — следующее число больше предыдущего (↑)
 * -1  — следующее число меньше предыдущего (↓)
 *  0  — программа воздерживается: недостаточно данных для этого правила
 */
export type Direction = 1 | -1 | 0;

// ─── Program ──────────────────────────────────────────────────────────────────

/**
 * Категория программы.
 * 'manual'  — написана вручную (тренд, возврат к средней, волатильность и т.д.)
 * 'pattern' — автогенерированная: проверяет конкретный паттерн длиной 2..7
 *             например ↑↓↓↑ → ↑
 */
export type ProgramCategory = 'pattern' | 'manual';

/**
 * Одна программа-предсказатель из пространства 500+ гипотез.
 *
 * name     — читаемое название, отображается в списке Programs
 * category — для фильтрации по вкладкам (ТОП / ПАТТЕРНЫ / РУЧНЫЕ)
 * fn       — чистая функция: принимает текущий ряд и историю направлений,
 *            возвращает Direction (0 если правило не применимо)
 */
export interface Program {
  name: string;
  category: ProgramCategory;
  /**
   * Возвращает мягкий голос в диапазоне [-1..1]:
   *   +1.0  — сильная уверенность ↑
   *   +0.5  — слабый сигнал ↑
   *    0.0  — нет мнения / воздерживается
   *   -0.5  — слабый сигнал ↓
   *   -1.0  — сильная уверенность ↓
   */
  fn: (sequence: number[], directions: Direction[]) => number;
}
// ─── Program stats ────────────────────────────────────────────────────────────

/**
 * Байесовское состояние одной программы. Хранится параллельно с ALL_PROGRAMS[i].
 *
 * weight  — текущий вес программы в байесовском голосовании.
 *           Начинается с 1.0.
 *           При правильном предсказании: weight *= 1.08
 *           При ошибке:                 weight *= 0.92
 *           Зажато в диапазон [0.001, 1000].
 *           Перед голосованием все веса нормируются через softmax,
 *           поэтому абсолютные значения не важны — важны только
 *           относительные соотношения между программами.
 *
 * correct — счётчик верных предсказаний этой программы (для UI)
 * total   — счётчик всех предсказаний этой программы (для UI)
 *           acc = correct / total → отображается рядом с весом в Programs.tsx
 */
export interface ProgramStat {
  weight: number;
  correct: number;
  total: number;
}

// ─── CTW ──────────────────────────────────────────────────────────────────────

/**
 * Один узел контекстного дерева (Context Tree Weighting).
 *
 * up   — сколько раз после данного контекста следовало направление ↑
 * down — сколько раз после данного контекста следовало направление ↓
 *
 * Начальные значения: up=1, down=1 (псевдосчёт, сглаживание Лапласа).
 * P(↑ | контекст) = up / (up + down)
 */
export interface CTWNode {
  up: number;
  down: number;
}

/**
 * Иерархическая контекстная модель из 4 глубин.
 *
 * Структура: { [depth]: { [contextKey]: CTWNode } }
 *
 * depth 1: ключи длиной 1 — "U", "D"
 * depth 2: ключи длиной 2 — "UU", "UD", "DU", "DD"
 * depth 3: ключи длиной 3 — "UUU", "UUD", ... (8 вариантов)
 * depth 4: ключи длиной 4 — "UUUU", ... (16 вариантов)
 *
 * Пример: ctxModel[3]["UDD"] = { up: 7, down: 3 }
 * → после паттерна ↑↓↓ система видела ↑ семь раз и ↓ три раза
 *
 * При смешивании глубин используется вес: log(наблюдений + 1) / depth²
 * То есть глубокий контекст влияет меньше, пока не накоплено достаточно данных.
 */
export type CTWModel = Record<number, Record<string, CTWNode>>;

// ─── Prediction ───────────────────────────────────────────────────────────────

/**
 * Снимок предсказания в конкретный момент времени.
 * Создаётся в predictor.ts::predict() и сохраняется как pendingPrediction.
 * Когда приходит следующее число — снимок используется для обновления весов,
 * затем заменяется новым.
 *
 * direction    — итоговое предсказание: 1 (↑) или -1 (↓)
 *
 * pUp          — итоговая вероятность P(↑) после байесовского смешивания
 *                трёх источников: программы + CTW + LZ78.
 *                Диапазон [0, 1]. Отображается в полосе PredictionBox.
 *
 * pUpPrograms  — вклад только от программ (softmax-взвешенное голосование).
 *                Всегда присутствует (≠ null).
 *
 * pUpCTW       — вклад от иерархической CTW модели.
 *                null если ни для одной глубины нет данных в ctxModel.
 *
 * compPred     — Direction от LZ78 компрессора (1, -1, или 0 если данных < 4).
 *                0 не означает "не знаю P(↑)" — означает "нет сигнала".
 *
 * pUpComp      — вероятность от компрессора: 0.65 если compPred=1,
 *                0.35 если compPred=-1, null если compPred=0.
 *
 * programPreds — КЛЮЧЕВОЕ поле. Массив Direction[], индекс = индекс в ALL_PROGRAMS.
 *                Зафиксированные предсказания каждой программы в момент создания снимка.
 *                Используется в updateWeights() вместо повторного вычисления fn().
 *                Это исправляет баг первой версии: программы больше не пересчитываются
 *                на уже обновлённых данных — только на тех, что были при предсказании.
 */
export interface Prediction {
  direction: Direction;
  pUp: number;
  pUpPrograms: number;
  pUpCTW: number | null;
  compPred: Direction;
  pUpComp: number | null;
  /** Мягкие голоса [-1..1] каждой программы в момент снимка */
  programPreds: number[];
}

// ─── Log entry ────────────────────────────────────────────────────────────────

/**
 * Одна строка в истории предсказаний (HistoryLog.tsx).
 *
 * n        — порядковый номер предсказания (1, 2, 3, ...)
 * prev     — число до шага (левая сторона перехода)
 * curr     — число после шага (правая сторона перехода)
 * pred     — что предсказала система (1=↑, -1=↓)
 * actual   — что произошло на самом деле (1=↑, -1=↓)
 * correct  — pred === actual
 * pUp      — уверенность системы в момент предсказания (P(↑))
 * compPred — что говорил LZ78 в этот момент (для колонки "LZ↑/LZ↓")
 */
export interface LogEntry {
  n: number;
  prev: number;
  curr: number;
  pred: Direction;
  actual: Direction;
  correct: boolean;
  pUp: number;
  compPred: Direction;
}

// ─── App stats ────────────────────────────────────────────────────────────────

/**
 * Агрегированная статистика предсказаний.
 * Вынесена отдельно от AppState чтобы Statistics.tsx получал только нужные данные,
 * а не весь стейт целиком.
 *
 * total   — сколько всего предсказаний было сделано
 * correct — сколько из них оказались верными
 * accuracy = correct / total
 */
export interface AppStats {
  total: number;
  correct: number;
}

// ─── App state ────────────────────────────────────────────────────────────────

/**
 * Полное состояние приложения. Живёт в useReducer в Home.tsx.
 * Намеренно иммутабельное — все функции в logic/predictor.ts
 * принимают AppState и возвращают новый AppState без мутаций.
 * Это позволяет AUTO×100 работать в синхронном цикле без ререндеров.
 *
 * sequence          — исходный числовой ряд, введённый пользователем.
 *                     Например: [7, 6, 2, 1, 4]
 *
 * directions        — производный ряд направлений, длина = sequence.length - 1.
 *                     directions[i] = sign(sequence[i+1] - sequence[i])
 *                     Например: [-1, -1, -1, 1]
 *
 * pendingPrediction — снимок текущего предсказания (для следующего числа).
 *                     null если в sequence меньше 2 элементов.
 *                     Заменяется новым снимком после каждого добавления числа.
 *
 * stats             — суммарная статистика: total и correct предсказаний.
 *
 * accHistory        — история точности после каждого шага [0..1].
 *                     accHistory[i] = correct[i] / total[i]
 *                     Используется для рисования графика в Statistics.tsx.
 *
 * logHistory        — история предсказаний в обратном порядке (новые первые).
 *                     Отображается в HistoryLog.tsx. Ограничена выводом 60 строк.
 *
 * progStats         — массив длиной ALL_PROGRAMS.length.
 *                     progStats[i] соответствует ALL_PROGRAMS[i].
 *                     Хранит веса и счётчики каждой программы.
 *
 * ctxModel          — иерархическая CTW модель (4 глубины).
 *                     Обновляется в updateWeights() используя контекст
 *                     ДО текущего шага (directions перед добавлением нового элемента).
 */
export interface AppState {
  sequence: number[];
  directions: Direction[];
  pendingPrediction: Prediction | null;
  stats: AppStats;
  accHistory: number[];
  logHistory: LogEntry[];
  progStats: ProgramStat[];
  ctxModel: CTWModel;
}